package IYou;



public class Salary {

   public Salary() {
   }


   private int holiday_allowance;

   public int getHoliday_allowance() {
      return holiday_allowance;
   }
   
   public void setHoliday_allowance(int holiday_allowance) {
      this.holiday_allowance = holiday_allowance;
   }      

   private int monthly_pay;

   public int getMonthly_pay() {
      return monthly_pay;
   }
   
   public void setMonthly_pay(int monthly_pay) {
      this.monthly_pay = monthly_pay;
   }      

   private int bonus;

   public int getBonus() {
      return bonus;
   }
   
   public void setBonus(int bonus) {
      this.bonus = bonus;
   }      

   private int yearly_salary;

   public int getYearly_salary() {
      return yearly_salary;
   }
   
   public void setYearly_salary(int yearly_salary) {
      this.yearly_salary = yearly_salary;
   }      

}
